#לכתוב סקריפט שידרוש ממני לכתוב מספר והמערכת תדפיס את כל המספרים שמחלקים את המספר בטווח מסויים שאני מגדיר
num = int(input("Please enter a number: "))

for i in range(1, 100000000000000000): #לא לכתוב 0 על מנת שלא תהיה שגיעה כי אי אפשר לחלק מספרים ב 0
    if (int(num) % int(i) == 0): #אם המספר שאני מכניס מתחלק במספר בטווח של 1- 100000000000000000
        print(i)
#חשוב לכתוב נקודותיים אחרי כל תנאי כדי שהסקריפט לא יכשל